private void saveCatatanToFile() {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter("catatan.txt", true))) {
        for (String[] catatanData : catatanList) {
            writer.write(String.join(",", catatanData));  // Menggabungkan data dengan koma
            writer.newLine();  // Menambahkan baris baru setelah setiap catatan
        }
        JOptionPane.showMessageDialog(this, "Catatan berhasil disimpan.");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan catatan.");
        e.printStackTrace();
    }}
    
    private void loadCatatanFromFile() {
    File file = new File("catatan.txt");
    if (!file.exists()) {
        return;  // Jika file tidak ada, tidak perlu melakukan apa-apa
    }

    try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] catatanData = line.split(",");
            catatanList.add(catatanData);  // Menambahkan catatan yang dibaca ke dalam list
        }
        updateTabel();  // Memperbarui tabel setelah memuat data
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat catatan.");
        e.printStackTrace();
    }}
    
    
