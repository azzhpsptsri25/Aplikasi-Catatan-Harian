    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
                                            
        String topik = topikField.getText();
    String hari = (String) hariComboBox.getSelectedItem();
    String tanggal = tanggalField.getText();
    String mood = (String) moodComboBox.getSelectedItem();
    String catatan = jTextArea.getText();

    if (topik.isEmpty() || tanggal.isEmpty() || catatan.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap isi semua field.");
    } else {
        // Simpan catatan ke dalam array atau list untuk ditambahkan ke file
        String[] catatanData = {topik, hari, tanggal, mood, catatan};
        catatanList.add(catatanData);
        
        // Update tampilan tabel (misalnya JTable)
        updateTabel();

        // Simpan catatan ke file
        saveCatatanToFile();

        // Clear fields after saving
        topikField.setText("");
        tanggalField.setText("");
        jTextArea.setText("");
    }
}
    
    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = catatanTable.getSelectedRow();
    if (selectedRow != -1) {
        // Mendapatkan data yang dipilih dari tabel
        String topik = (String) catatanTable.getValueAt(selectedRow, 0);
        String hari = (String) catatanTable.getValueAt(selectedRow, 1);
        String tanggal = (String) catatanTable.getValueAt(selectedRow, 2);
        String mood = (String) catatanTable.getValueAt(selectedRow, 3);
        String catatan = (String) catatanTable.getValueAt(selectedRow, 4);

        // Menampilkan data yang dipilih di form untuk diedit
        topikField.setText(topik);
        hariComboBox.setSelectedItem(hari);
        moodComboBox.setSelectedItem(mood);
        tanggalField.setText(tanggal);
        jTextArea.setText(catatan);
    }
}
            
    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = catatanTable.getSelectedRow();
        if (selectedRow != -1) {
            catatanList.remove(selectedRow);
            updateTabel();
        } else {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris untuk menghapus data.");
        }
        }
        
    private void updateTabel() {
        DefaultTableModel model = (DefaultTableModel) catatanTable.getModel();
        model.setRowCount(0);
        for (String[] catatanData : catatanList) {
            model.addRow(catatanData);
        }
        
    
    }//GEN-LAST:event_simpanButtonActionPerformed

